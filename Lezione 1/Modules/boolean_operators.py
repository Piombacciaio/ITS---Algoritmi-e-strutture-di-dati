def main():
    val_1 = True
    val_2 = False
    print("Operatori booleani in Python:")
    print(f"val_1: {val_1}, val_2: {val_2}")
    print("--------------------------------")
    print(f"val_1 AND val_2: {val_1 and val_2}")
    print(f"val_1 OR val_2: {val_1 or val_2}")
    print(f"NOT val_1: {not val_1}")
    print(f"val_1 XOR val_2: {val_1 ^ val_2}")
    print(f"val_1 == val_2: {val_1 == val_2}")
    print(f"val_1 != val_2: {val_1 != val_2}")
    print(f"val_1 is val_2: {val_1 is val_2}")
    print(f"val_1 is not val_2: {val_1 is not val_2}")
    print(f"val_1 > val_2: {val_1 > val_2}")
    print(f"val_1 < val_2: {val_1 < val_2}")
    print(f"val_1 >= val_2: {val_1 >= val_2}")
    print(f"val_1 <= val_2: {val_1 <= val_2}")

if __name__ == "__main__":
    main()